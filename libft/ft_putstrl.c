#include "libft.h"

int	ft_putstrl(const char *s)
{
	int	i;

	i = 0;
	if (!s)
	{
		ft_putstrl("(null)");
		return (6);
	}
	while (s[i])
		write (1, &s[i++], 1);
	return (i);
}
